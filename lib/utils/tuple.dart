class Pair<T1, T2> {
  final T1 first;
  final T2 second;

  const Pair(this.first, this.second);
}

class Triple<T1, T2, T3> {
  final T1 first;
  final T2 second;
  final T3 third;

  const Triple(this.first, this.second, this.third);
}

class Quadruple<T1, T2, T3, T4> {
  final T1 first;
  final T2 second;
  final T3 third;
  final T4 forth;

  const Quadruple(this.first, this.second, this.third, this.forth);
}
